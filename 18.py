#Dictionary(sözlük)veri tipi 

sözlük={"book":"kitap","ismi":"utku","soyadı":"altunay","cinsiyeti":"erkek","car":"araba"}

print(sözlük.keys()) #keys:book,ismi,soyadı,car

print(sözlük.values()) #values:kitap,utku,altunay,araba

print(sözlük["ismi"]) # key in vaulesini gösterir

print(sözlük["book"]) 
print(sözlük["ismi"]) 
print(sözlük["soyadı"]) 
print(sözlük["cinsiyeti"]) 
print(sözlük["car"]) 